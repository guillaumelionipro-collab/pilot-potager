export function searchCultures(query) {
  const q = query.toLowerCase().trim();
  if (!q) return cultureLibrary;
  return cultureLibrary.filter(
    (c) => c.name.toLowerCase().includes(q) || c.family.toLowerCase().includes(q)
  );
}

export function getCulturesByCategory(cat) {
  return cultureLibrary.filter((c) => c.category === cat);
}
